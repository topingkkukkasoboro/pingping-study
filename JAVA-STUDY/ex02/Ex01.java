package ex02;

public class Ex01 {
	
	public static void doA() { // 나중에 배운다고함..
		int c = 30; // c에 int형으로 30을 부여
		int d = 40; // 이건 40부여
		System.out.println(c+d); // c와 d를 더한 값을 출력 70
	}

	public static void main(String[] args) {
	
		int a = 10; // a에 10을 부여
		int b = 20; // b에 20을 부여
		System.out.println(a+b); // a + b를 출
		
		doA(); // 나중에 배우게 된다고 한다 천천히 하자
		System.out.println("종료됩니다."); // 종료됩니다 출력
	}
	
	
}
