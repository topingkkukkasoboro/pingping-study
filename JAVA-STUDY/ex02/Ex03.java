package ex02;

public class Ex03 {
	
	public static void main(String[] args) {
		
		// 정수 선언 // 정수는 소수점이 없는 숫자
		short k = 20; // 짧아서 잘 사용안함
		int a = 10; // 제일 많이 사용하고 평균적인
		long f = 50; // 제일 긴 숫자를 사용할수있다
		// 실수 선언 // 실수는 소수점이 있는 숫자
		float b = 20.23f;// float는 숫자뒤에 f를 사용
		double c = 20.223d; // double은 d를 붙여서 사용
		
		// 문자 문자열 선언
		char d = 'A'; // char을 쓸때는 '' 이걸로 사용해야한다 "" 사용안됨 // char를 한개의 문자를 넣을수있다 ex)'A' 
		String e = "안녕하세요"; // 문장을 넣을 수 있다
		
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
		System.out.println(e);
		System.out.println(f);
		System.out.println(k);
		
	}

}
