package ex03;

public class Ex07 {


	public static void main(String[] args) {     
		
		System.out.println("a\tb\tc"); // \t는 탭만큼 줄을 띄워준다
		System.out.println(); // 아무것도 없으니 빈공백 출력
		System.out.println("ddd\beee\b"); // 한 라인을 띄운다
		
		// "안녕하세요"
		System.out.println("\"안녕하세요\""); // \"는 특수문자 출력
		
		// c:\
		System.out.println("c:\\");// \ 를 출력하기 위해서는 \\ 이런식으로 한번 더 입력
		
	}
}
