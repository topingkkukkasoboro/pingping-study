package ex03_1;

public class Ex01 {

	public static void main(String[] args) {
		System.out.println(3%2);// 3 나누기 2의 나머지는 1// 나머지를 구하는 방법은 %다
		
		// 0010 2진수
		// 1000 => 8
		System.out.println(2<<2);//2진수 바꾸기
		
		System.out.println(10>3); // 10은 3보다 크다 // true
		System.out.println(10<=3); //  10은 3보다 작거나 같다 // false
		
		System.out.println(10==10); // 10은 10과 같다 // true
		System.out.println(10!=10); // 10은 10과 다르다 // false
		
		int a = 10;
		int b = 20;
		
		System.out.println(a==b); // a랑 b는 같다 // false
		System.out.println(a!=b); // a는 b와 다르다 // true
		
	}
}
