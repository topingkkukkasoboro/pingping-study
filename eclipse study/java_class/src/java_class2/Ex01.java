package java_class2;

public class Ex01 {
	public static void main(String[] args) {
		System.out.println(3%2);
		// 모르겠음
		
		System.out.println(2<<2);
		// 비트연산자
		
		System.out.println(10>3);
		// 비교 연산자
		
		System.out.println(10<=3);
		//비교 연산자
		
		System.out.println(10==10);
		// 같은지 비교
		
		System.out.println(10!=10);
		// 다른지 비교
		
		int a = 10;
		int b = 20;
		
		System.out.println(a==b); // a랑 b가 같나요?
		System.out.println(a!=b); // a랑 b가 다른가요?
	}


}
